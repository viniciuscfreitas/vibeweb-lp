function initI18n() {
    const userLang = navigator.language || navigator.userLanguage;
    const langCode = userLang.split('-')[0]; // e.g. 'en-US' -> 'en'
    
    // Choose translation, fallback to English
    const translation = translations[langCode] || translations['en'];
    
    // Update elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translation[key]) {
            el.innerHTML = translation[key];
        }
    });

    // Update HTML lang attribute
    document.documentElement.lang = langCode;

    // Update metadata
    if (translation['hero-title']) {
        const cleanTitle = translation['hero-title'].replace('<br>', ' ').replace(/<[^>]*>?/gm, '');
        document.title = cleanTitle + ' â€“ VibeWeb';
    }

    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && translation['hero-subtitle']) {
        metaDesc.setAttribute('content', translation['hero-subtitle']);
    }
}

// Run on load
document.addEventListener('DOMContentLoaded', initI18n);

