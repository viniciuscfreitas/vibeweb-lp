function getLang() {
  const params = new URLSearchParams(window.location.search);
  const langParam = params.get("lang");

  if (langParam && translations[langParam]) {
    return langParam;
  }

  const browserLang = (
    navigator.language ||
    navigator.userLanguage ||
    "en"
  ).split("-")[0];

  return translations[browserLang] ? browserLang : "en";
}

function initI18n() {
  const lang = getLang();
  const dict = translations[lang];

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.innerHTML = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-meta]").forEach((el) => {
    const key = el.getAttribute("data-i18n-meta");
    if (dict[key]) {
      el.setAttribute("content", dict[key]);
    }
  });

  if (dict.title) {
    document.title = dict.title;
  }

  document.documentElement.lang = lang;

  // Initialize Cookie Banner Logic after translations are applied
  initCookieBanner();
}

function initCookieBanner() {
  const banner = document.getElementById("cookie-banner");
  const backdrop = document.getElementById("cookie-backdrop");
  if (!banner) return;

  const acceptBtn = document.getElementById("cookie-accept");
  const declineBtn = document.getElementById("cookie-decline");
  const consent = localStorage.getItem("vibe-cookie-consent");

  if (!consent) {
    setTimeout(() => {
      banner.classList.add("is-visible");
      if (backdrop) backdrop.classList.add("is-visible");
      // Accessibility: move focus to banner for screen readers
      banner.setAttribute("aria-hidden", "false");
    }, 1000);
  } else {
    banner.setAttribute("aria-hidden", "true");
  }

  acceptBtn.addEventListener("click", () => {
    localStorage.setItem("vibe-cookie-consent", "accepted");
    banner.classList.remove("is-visible");
    if (backdrop) backdrop.classList.remove("is-visible");
    banner.setAttribute("aria-hidden", "true");
    if (window.loadGTM) window.loadGTM();
  });

  declineBtn.addEventListener("click", () => {
    localStorage.setItem("vibe-cookie-consent", "declined");
    banner.classList.remove("is-visible");
    if (backdrop) backdrop.classList.remove("is-visible");
    banner.setAttribute("aria-hidden", "true");
  });
}

window.switchLang = function(lang) {
  if (!translations[lang]) return;

  // Apply translations
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  // Apply placeholder translations
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // Apply meta translations
  document.querySelectorAll('[data-i18n-meta]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-meta');
    if (translations[lang][key]) {
      el.setAttribute('content', translations[lang][key]);
    }
  });

  // Update page title
  if (translations[lang].title) {
    document.title = translations[lang].title;
  }

  // Update HTML lang
  document.documentElement.lang = lang;

  // Update active button
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.setAttribute('aria-current', btn.getAttribute('data-lang') === lang ? 'true' : 'false');
  });

  // Save preference
  localStorage.setItem('vibe-lang', lang);
};

